    public void updateCalendary()
    {
        Enumeration<AbstractButton> buttons = monthButtons.getElements();
        while (buttons.hasMoreElements())
        {
            AbstractButton button = buttons.nextElement();
            
            String buttonText = button.getText();
            String dayString = buttonText.length() < 2 ? buttonText : buttonText.substring(0, 2);
            int day = Integer.parseInt(dayString);
            String date = Activity.parseDate(day, month, year);
            
            int count = calendary.getActivityCollection().getActivitiesInDate(date).size();
            if (count > 0)
            {
                String text = dayString + " (" + count + ")";
                button.setText(text);
            }
            else
            {
                String text = day <= 9 ? "0" + Integer.toString(day) : Integer.toString(day);
                button.setText(text);
            }
        }
        
        monthContext.setVisibleButtons(day29, day30, day31);
    }
    
    private void dayActionPerformed(int day, javax.swing.JButton button)
    {
        guiActivity.setVisible(true);
        
        ArrayList<Activity> activities = calendary
                .getActivityCollection()
                .getActivitiesInDate(day, month, year);
        guiActivity.setDate(day, month, year);
        guiActivity.openCurrentDay(activities);
    }
    
