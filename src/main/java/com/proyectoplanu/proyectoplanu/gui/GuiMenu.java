    private void previousMonthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousMonthActionPerformed
        month--;
        if (month < 1)
            month = 12;
        monthLabel.setText(getMonth() + " de");
        updateMonthContext();
        updateCalendary();
    }//GEN-LAST:event_previousMonthActionPerformed

    private void nextMonthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextMonthActionPerformed
        month++;
        if (month > 12)
            month = 1;
        monthLabel.setText(getMonth() + " de");
        updateMonthContext();
        updateCalendary();
    }//GEN-LAST:event_nextMonthActionPerformed

    private void yearSpinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_yearSpinnerStateChanged
        year = (int)yearSpinner.getValue();
        if (year < 2022)
        {
            yearSpinner.setValue(2022);
            year = 2022;
        }
        updateCalendary();
    }//GEN-LAST:event_yearSpinnerStateChanged
    
    private void updateMonthContext()
    {
        int days = (int)(28 + (month + Math.floor(month/8)) % 2 + 2 % month + 2 * Math.floor(1/month));
        switch (days)
        {
            case 28:
                monthContext.swapStrategy(new TwentyEightDayMonth());
                break;
            case 30:
                monthContext.swapStrategy(new ThertyDayMonth());
                break;
            case 31:
                monthContext.swapStrategy(new ThertyOneDayMonth());
                break;
        }
    }
    
    private String getMonth()
    {
        switch (month)
        {
            case 1:
                return "Enero";
            case 2:
                return "Febrero";
            case 3:
                return "Marzo";
            case 4:
                return "Abril";
            case 5:
                return "Mayo";
            case 6:
                return "Junio";
            case 7:
                return "Julio";
            case 8:
                return "Agosto";
            case 9:
                return "Septiembre";
            case 10:
                return "Octubre";
            case 11:
                return "Noviembre";
            case 12:
                return "Diciembre";
            default:
                return "Error";
        }
    }
    
